import Head from 'next/head'
import styles from '../styles/Home.module.css'
import Banner from "../components/banner/banner";
import NavBar from "../components/nav/navbar";
import SectionCards from "../components/card/section-cards";
import {getVideos, getPopularVideos, getWatchItAgainVideos} from "../lib/videos";
import useRedirectUser from "../utils/redirectUser";

export async function getServerSideProps(context) {
  const {userId, token} = await useRedirectUser(context);
    if (!userId) {
        return {
            props: {},
            redirect: {
                destination:"/login",
                permanent: false,
            },
        };
    }

  const watchItAgainVideos = await getWatchItAgainVideos(userId, token);


  const disneyVideos = await getVideos("disney trailer");
  const productivityVideos = await getVideos("productivity");
  const travelVideos = await getVideos("travel");
  const popularVideos = await getPopularVideos("popular videos");
  return { props: {
      watchItAgainVideos,
      disneyVideos,
      productivityVideos,
      travelVideos,
      popularVideos },
  };
}
export default function Home({watchItAgainVideos,
                               disneyVideos,
                               productivityVideos,
                               travelVideos,
                               popularVideos  }) {

  return (
    <div className={styles.container}>
      <Head>
        <title>Netflix</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
        <div className={styles.main}>
          <NavBar />
          <Banner
              videoId="4zH5iYM4wJo"
              title='Clifford is the red dog'
              subtitle='He is a smart dog'
              imgUrl='/static/clifford.jpg'
          />
          <div className={styles.sectionWrapper}>
            <SectionCards title="Disney" videos={disneyVideos} size="large"/>
            <SectionCards title="Watch Again" videos={watchItAgainVideos} size="small"/>
            <SectionCards title="Travel" videos={travelVideos} size="small"/>
            <SectionCards title="Productivity" videos={productivityVideos} size="medium"/>
            <SectionCards title="Popular" videos={popularVideos} size="small"/>
          </div>
        </div>
    </div>
  )
}
